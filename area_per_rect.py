# This program calculates and displays the area and perimeter of a
# rectangle with length 3.8cm and width 7.5cm.


def main():
    print("For a rectangle that has dimensions")
    print("length 3.8cm and width 7.5cm:")
    print("")
    print("Area = {} cm^2".format(3.8 * 7.5))
    print("Perimeter = {} cm".format(2 * (3.8 + 7.5)))


if __name__ == "__main__":
    main()
